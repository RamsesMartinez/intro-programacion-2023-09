"""
3. Crea un diccionario con 3 pares clave-valor representando un libro
    (título, autor, año)
- añade el número de páginas, modificando el diccionario

"""
book = {
    "title": "Cien años de soledad",
    "author": "Gabriel García Márquez",
    "year": 1967
}
book["pages"] = 417
print(
    book
)  # {'título': 'Cien años de soledad', 'autor': 'Gabriel García Márquez', 'año': 1967, 'páginas': 417}
